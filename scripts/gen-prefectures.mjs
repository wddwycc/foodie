// Aggregate every scraped set by prefecture, so the map can show all 百名店
// restaurants in a prefecture across genres. Derives the prefecture from each
// restaurant's address — no re-scraping needed.
//
//   node scripts/gen-prefectures.mjs
//
// Writes:
//   public/data/v4/pref/<key>.json     restaurants in that prefecture (+ genre)
//   app/prefectures.generated.ts    list of non-empty prefectures (+ counts)

import { readdir, readFile, writeFile, mkdir, rm } from "node:fs/promises";
import path from "node:path";
import { loadSetCatalog } from "./lib/catalog.mjs";
import {
  PREFECTURES,
  REGION_ORDER,
  prefectureOf,
} from "./lib/prefectures.mjs";

const DATA_DIR = path.join(process.cwd(), "public", "data", "v4");
const PREF_DIR = path.join(DATA_DIR, "pref");
const OUT_FILE = path.join(process.cwd(), "app", "prefectures.generated.ts");

const catalog = await loadSetCatalog();
const genreBySlug = new Map(catalog.map((c) => [c.slug, c.genre]));

// Collect restaurants per prefecture key.
const byPref = new Map(); // key -> Restaurant[]
const files = (await readdir(DATA_DIR)).filter((f) => f.endsWith(".json"));

for (const file of files) {
  const slug = file.replace(/\.json$/, "");
  const genre = genreBySlug.get(slug);
  if (!genre) continue; // skip non-set files
  const rows = JSON.parse(await readFile(path.join(DATA_DIR, file), "utf8"));
  for (const r of rows) {
    const pref = prefectureOf(r.address);
    if (!pref) continue;
    const list = byPref.get(pref.key) ?? [];
    list.push({ ...r, genre });
    byPref.set(pref.key, list);
  }
}

// Rewrite the pref/ directory from scratch.
await rm(PREF_DIR, { recursive: true, force: true });
await mkdir(PREF_DIR, { recursive: true });

const present = [];
for (const pref of PREFECTURES) {
  const list = byPref.get(pref.key);
  if (!list || list.length === 0) continue;
  // Sort by rating desc so the strongest restaurants read first.
  list.sort((a, b) => (b.rating ?? 0) - (a.rating ?? 0));
  await writeFile(
    path.join(PREF_DIR, `${pref.key}.json`),
    JSON.stringify(list, null, 2),
  );
  present.push({ ...pref, count: list.length });
}

// Group present prefectures by region in canonical order.
const groups = REGION_ORDER.map((region) => ({
  region,
  items: present
    .filter((p) => p.region === region)
    .map(({ key, ja, count }) => ({ key, ja, count })),
})).filter((g) => g.items.length > 0);

const body = `// AUTO-GENERATED by scripts/gen-prefectures.mjs — do not edit by hand.
import type { PrefectureGroup } from "./prefectures";

export const PREFECTURE_GROUPS: PrefectureGroup[] = ${JSON.stringify(
  groups,
  null,
  2,
)};
`;
await writeFile(OUT_FILE, body);

const total = present.reduce((n, p) => n + p.count, 0);
console.log(
  `Wrote ${present.length} prefectures (${total} restaurants) -> ${PREF_DIR}`,
);
console.log(`Wrote ${OUT_FILE}`);
